
class Training {
  final String date;
  final String time;
  final String title;
  final String status;
  final String location;
  final String trainer;
  final String image;
  final String summary;

  Training({
    required this.date,
    required this.time,
    required this.title,
    required this.status,
    required this.location,
    required this.trainer,
    required this.image,
    required this.summary,
  });
}
